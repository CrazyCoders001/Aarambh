#include <iostream>
using namespace std;

int main() {
    // Integer types
    int myInt = 10;
    short myShort = 5;
    long myLong = 123456;
    long long myLongLong = 1234567890;

    // Floating-point types
    float myFloat = 3.14f;
    double myDouble = 3.1415926535;
    long double myLongDouble = 3.141592653589793;

    // Character type
    char myChar = 'A';

    // Boolean type
    bool myBool = true;

    // Display all values
    cout << "int: " << myInt << endl;
    cout << "short: " << myShort << endl;
    cout << "long: " << myLong << endl;
    cout << "long long: " << myLongLong << endl;
    cout << "float: " << myFloat << endl;
    cout << "double: " << myDouble << endl;
    cout << "long double: " << myLongDouble << endl;
    cout << "char: " << myChar << endl;
    cout << "bool: " << myBool << endl;


    return 0;
}
